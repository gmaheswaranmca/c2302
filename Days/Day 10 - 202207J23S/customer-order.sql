CREATE TABLE  "AGENTS" 
(	
"AGENT_CODE" VARCHAR(6) NOT NULL PRIMARY KEY, 
"AGENT_NAME" VARCHAR(40), 
"WORKING_AREA" VARCHAR(35), 
"COMMISSION" FLOAT, 
"PHONE_NO" VARCHAR(15), 
"COUNTRY" VARCHAR(25) 
);

sp_help AGENTS;
 
CREATE TABLE  "CUSTOMER" 
(	"CUST_CODE" VARCHAR(6) NOT NULL PRIMARY KEY, 
"CUST_NAME" VARCHAR(40) NOT NULL, 
"CUST_CITY" VARCHAR(35), 
"WORKING_AREA" VARCHAR(35) NOT NULL, 
"CUST_COUNTRY" VARCHAR(20) NOT NULL, 
"GRADE" INT, 
"OPENING_AMT" FLOAT NOT NULL, 
"RECEIVE_AMT" FLOAT NOT NULL, 
"PAYMENT_AMT" FLOAT NOT NULL, 
"OUTSTANDING_AMT" FLOAT NOT NULL, 
"PHONE_NO" VARCHAR(17) NOT NULL, 
"AGENT_CODE" VARCHAR(6) NOT NULL
);   
sp_help CUSTOMER;

CREATE TABLE  "ORDERS" 
(
"ORD_NUM" INT NOT NULL PRIMARY KEY, 
"ORD_AMOUNT" FLOAT NOT NULL, 
"ADVANCE_AMOUNT" FLOAT NOT NULL, 
"ORD_DATE" DATETIME NOT NULL, 
"CUST_CODE" VARCHAR(6) NOT NULL, 
"AGENT_CODE" VARCHAR(6) NOT NULL, 
"ORD_DESCRIPTION" VARCHAR(60) NOT NULL
);
sp_help ORDERS;


